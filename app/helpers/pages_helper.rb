  # Format an amount of cents as BRL, dropping ",00" for whole values.
  # 9_900 → "R$ 99" · 129_900 → "R$ 1.299" · 9_990 → "R$ 99,90"
  def brl_short(cents)
    reais  = cents / 100
    rest   = cents % 100
    grouped = reais.to_s.reverse.gsub(/(\d{3})(?=\d)/, '\\1.').reverse
    rest.zero? ? "R$ #{grouped}" : format('R$ %s,%02d', grouped, rest)
  end

